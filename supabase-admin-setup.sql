create or replace view public.property_admin_metrics as
select
  p.id,
  p.title,
  p.slug,
  p.category,
  p.status,
  p.featured,
  p.created_at,
  count(distinct v.id) as views_count,
  count(distinct f.id) as favorites_count,
  count(distinct l.id) as leads_count
from public.properties p
left join public.property_views v on v.property_id = p.id
left join public.property_favorites f on f.property_id = p.id
left join public.leads l on l.property_id = p.id
group by p.id;

