create or replace function public.handle_new_auth_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  role_to_apply text;
begin
  -- O primeiro usuario criado vira admin automaticamente.
  if not exists (select 1 from public.user_roles where role = 'admin') then
    role_to_apply := 'admin';
  else
    role_to_apply := 'corretor';
  end if;

  insert into public.user_roles (id, role)
  values (new.id, role_to_apply)
  on conflict (id) do nothing;

  insert into public.brokers (id, email, name)
  values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data->>'name', new.email)
  )
  on conflict (id) do update set
    email = excluded.email,
    updated_at = now();

  return new;
end;
$$;

drop trigger if exists on_auth_user_created_privilege on auth.users;
create trigger on_auth_user_created_privilege
after insert on auth.users
for each row execute function public.handle_new_auth_user();

create or replace function public.set_user_role(target_email text, new_role text)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  target_id uuid;
begin
  if new_role not in ('admin', 'corretor') then
    raise exception 'Role invalida: %', new_role;
  end if;

  -- Permite configurar o primeiro admin mesmo se ainda nao existir admin.
  if exists (select 1 from public.user_roles where role = 'admin')
     and not public.is_admin() then
    raise exception 'Apenas administradores podem alterar permissoes.';
  end if;

  select id into target_id
  from auth.users
  where email = target_email
  limit 1;

  if target_id is null then
    raise exception 'Usuario nao encontrado: %', target_email;
  end if;

  insert into public.user_roles (id, role, updated_at)
  values (target_id, new_role, now())
  on conflict (id) do update set
    role = excluded.role,
    updated_at = now();
end;
$$;

